                    ? data[element][key].substring(0, 50) + `...`
                    : data[element][key]
            }
            <div>
                <span><i class="fas fa-file fa-1x text-success" style="visibility:hidden"></i></span>
                ${
                data[element]['diffforhuman']
                    ? data[element]['diffforhuman']
                    : ""
            }
                ${
                data[element]['read_at']
                    ?'<span> ReadAt: </span>'
                    : ""
            }
            ${
                data[element]['read_at']
                    ?  data[element]['read_at']
                    : ""
            }
            </div>
        </a>
        </div>`;
                    }
                });
                htmlOutput += `</div>`;
            }
            //If no data is there;
            if (Object.keys(data).length == 0) {
